<template>
  <div class="login-container">
    <!-- 动态背景装饰 -->
    <div class="bg-decoration">
      <div class="circle circle-1"></div>
      <div class="circle circle-2"></div>
      <div class="circle circle-3"></div>
    </div>

    <div class="login-box">
      <div class="login-header">
        <div class="logo-container">
          <img src="../../assets/logo.png" alt="logo" class="logo" />
        </div>
        <h2 class="title">图书管理系统</h2>
        <p class="subtitle">BOOK MANAGEMENT SYSTEM</p>
      </div>

      <el-form :model="loginForm" :rules="rules" ref="loginFormRef" size="large">
        <el-form-item prop="username">
          <el-input v-model="loginForm.username" placeholder="用户名">
            <template #prefix>
              <el-icon><User /></el-icon>
            </template>
          </el-input>
        </el-form-item>
        <el-form-item prop="password">
          <el-input
            v-model="loginForm.password"
            type="password"
            placeholder="密码"
            show-password
            @keyup.enter="handleLogin"
          >
            <template #prefix>
              <el-icon><Lock /></el-icon>
            </template>
          </el-input>
        </el-form-item>
        
        <div class="login-options">
          <el-checkbox v-model="rememberMe">记住我</el-checkbox>
          <el-button link type="primary" @click="router.push('/forgot-password')">忘记密码？</el-button>
        </div>

        <el-button
          :loading="loading"
          type="primary"
          class="login-button"
          @click="handleLogin"
        >
          {{ loading ? '登 录 中...' : '立 即 登 录' }}
        </el-button>
      </el-form>

      <div class="login-footer">
        <div class="divider">
          <span>测试账号</span>
        </div>
        <div class="test-accounts">
          <el-tag size="small" type="info">管理员: admin / admin123</el-tag>
